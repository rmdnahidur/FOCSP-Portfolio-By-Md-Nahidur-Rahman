"""
Module Name: converter
Description:
This module contains functions for unit conversion:
1. kilometers_to_miles - converts kilometers to miles
2. celsius_to_fahrenheit - converts Celsius to Fahrenheit
"""

# Function to convert kilometers to miles
def kilometers_to_miles(km):
    return km * 0.621371

# Function to convert Celsius to Fahrenheit
def celsius_to_fahrenheit(celsius):
    return (celsius * 9/5) + 32
