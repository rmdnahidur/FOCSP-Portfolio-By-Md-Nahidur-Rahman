

# Import the math_utils module
import math_utils

# Take input from user
num = int(input("Enter a number: "))

# Calculate factorial
fact = math_utils.factorial(num)
print(f"Factorial of {num} is: {fact}")

# Calculate square
sq = math_utils.square(num)
print(f"Square of {num} is: {sq}")

# Calculate cube
cb = math_utils.cube(num)
print(f"Cube of {num} is: {cb}")
